/** ISO 3166-1 alpha-2 → supported fiat when it matches `row.currency` (NGN bank transfer only). */
export const BANK_TRANSFER_ENABLED_ISO2_TO_FIAT = {
  NG: "NGN",
  KE: "KES",
} as const;

export type BankTransferEnabledFiat =
  (typeof BANK_TRANSFER_ENABLED_ISO2_TO_FIAT)[keyof typeof BANK_TRANSFER_ENABLED_ISO2_TO_FIAT];

export function getBankTransferEnabledFiat(
  row: BankTransferDirectoryRow,
): BankTransferEnabledFiat | null {
  const iso = row.iso2 as keyof typeof BANK_TRANSFER_ENABLED_ISO2_TO_FIAT;
  const expected = BANK_TRANSFER_ENABLED_ISO2_TO_FIAT[iso];
  if (expected === undefined || row.currency !== expected) return null;
  return expected;
}

/** Regional indicator symbol pair for a country code (fallback when no local flag asset). */
export function bankTransferDirectoryFlagEmoji(iso2: string): string {
  const u = iso2.toUpperCase();
  if (u.length !== 2 || !/^[A-Z]{2}$/.test(u)) return "\u2753";
  const cp = (ch: string) => 0x1f1e6 + (ch.charCodeAt(0) - 65);
  return String.fromCodePoint(cp(u[0]!), cp(u[1]!));
}